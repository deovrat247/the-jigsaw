						<h2> International History of Animation Studios and illustration </h2>
						<p> Illustrations have been used by human beings since time immemorial to convey incidents, stories and thoughts. An animation can often be thought of as a series of illustrations that are able to send a message as a series of images. While today with the presence of high end computers and a large number of studios, animation has become relatively easy. This was not always the case. But we digress, let us continue with the story of animation. This use of sequence of images dates back to ancient Indians, Egyptians, Greeks and romans. After the invention of the printing press the same illustrations could be mass printed and took the form of cartoons and comics. One of the oldest comic books printed was in Japan in the year 1755. It was published by KoikawaHarumachi and named 'Kinkin sensei eiga no yume' (Master Flashgold's Splendiferous Dream). It was a story about a poor, low-ranked samurai who travels from the Japanese countryside to Tokyo to find a better job.</p>
<img class="img-fluid" src="https://thejigsaw.in/assets/images/new/animation-on-text.jpg" alt="animation on text">
						<p>From comics to film animation took another 133 years, when the first true animation films was showcased by <strong> <a class="text-primary" href="https://en.wikipedia.org/wiki/Charles-%C3%89mile_Reynaud">Charles-Émile Reynaud.</a> </strong> His Pantomimes Lumineuses series of animated films each contained 300 to 700 frames, that were
							manipulated back and forth to give an effect of continuous motion.</p>
							
						<p>Between 1895 and 1920, during the rise of the cinematic industry, various  techniques were developed, including stop-motion with objects, puppets, clay or cutouts, and drawn or painted. Hand-drawn, mostly illustrations painted on cells, was the dominant technique throughout most of the 20th century and became known as traditional animation.</p>


						<p>The first 2D animated film ever made was called Fantasmagorie, which was made by Emile Cohl. The black and white cartoon begins with Emile drawing a simple stick man in live action.The cartoon is about 75 seconds long and it took about 700 different drawings to create. This historic work was released in 1908. </p>
						
						<p> This time also saw the start of the animation firms. The first animation studio in the world was founded by Raoul Barré and his studio, Barré Studio in the year 1914. Although some sources say that the credit for this should go to rival Bray Productions. </p>

						<p>The earliest practices adopted by these studios was the use of mechanical perforation of cells and animating special effects on glass.</p>

						<p> And the first use of the techniques was for making animated advertisement movies. </p>

						<p> The use of 3D came on much later and the initial 3D animated videos were made using stop motion or claymation. This was done by using real life elements like toys, miniatures and similar elements and shooting the videos by moving them bit by bit. This created the illusion of motion. This style of animation was used quite a lot by Aardman Animations. They are a well renowned studio, that is specifically known for the work that they have done in stop motion. They have made series like Gumby, Shawn The Sheep and Wallace and Gromit.</p>
						
						<p> One of the most famous companies that made the world's first all-CGI movie is Pixar company. Founded in 1986, they created Toy Story in 1995 and they have made more than 20 full movies to date.The company has had a great role in creating technological innovation in the animation field.</p>



