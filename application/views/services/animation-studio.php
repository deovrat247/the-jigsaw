                        <h2>Animation Studios In India 2021</h2>

						<p style="color:#000;"> In India the pioneers of <a class="text-primary" href="https://videoexplainermumbai.in/"> animation studios</a> have been The Nation Films Division of India. They started India’s first dedicated animation studio in 1957 with the production of The Banyan Deer. This studio located in Mumbai heralded the era of <a href="https://www.thejigsaw.in/Animation-Studio.html">animated videos</a> for the country. They were the first institution to focus on Indian animation content.</p>

						<p style="color:#000;">The film was made by the Films Division of India and Veteran animators Bhimsain& Ram Mohan were instrumental in making the film. For this they were trained by Clair Weeks and started their career in Film Division’s Cartoon Unit.</p>

						<p style="color:#000;"> When Clair Weeks came to India for this project, he had spent over 16 years in Disney Studios and was credited in the making of classics like Snow White, Cinderella and Bambi.</p>

						<p style="color:#000;"> The first Indian animated film made in a proper animation process was The Banyan Deer made in 1957.</p>

