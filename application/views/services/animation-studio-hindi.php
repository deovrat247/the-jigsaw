 </p>

						<p> ३डी एनिमेशन का उपयोग बहुत बाद में हुआ और प्रारंभिक ३डी एनिमेटेड वीडियो स्टॉप मोशन या क्ले मोशन का उपयोग करके बनाए गए। यह वास्तविक जीवन तत्वों जैसे खिलौने, लघुचित्र और इसी तरह के तत्वों का उपयोग करके और वीडियो को थोड़ा-थोड़ा करके शूट करके किया गया था। इसने गति का भ्रम पैदा किया। एनिमेशन की इस शैली का एर्डमैन एनिमेशन द्वारा काफी उपयोग किया गया था। वे एक प्रसिद्ध एनीमेशन स्टूडियो हैं, जो विशेष रूप से उस काम के लिए जाने जाते हैं जो उन्होंने स्टॉप मोशन एनीमेशन में किया है। उन्होंने गम्बी, शॉन द शीप और वालेस और ग्रोमिट जैसी श्रृंखलाएं बनाई हैं।

