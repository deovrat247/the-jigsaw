<h2>भारत में एनिमेशन स्टूडियो 2021</h2>


						<p style="color:#000;"> भारत में एनिमेशन स्टूडियो के अग्रदूत भारत के द नेशन फिल्म्स डिवीजन रहे हैं। उन्होंने वर्ष 1957 में द बरगद हिरण के निर्माण के साथ भारत का पहला समर्पित एनीमेशन स्टूडियो शुरू किया। मुंबई में स्थित इस स्टूडियो ने देश के लिए एनिमेशन के युग की शुरुआत की। वे भारतीय एनिमेशन सामग्री पर ध्यान केंद्रित करने वाले पहले संस्थान थे।</p>

						<p style="color:#000;"> यह फिल्म भारत के फिल्म प्रभाग द्वारा बनाई गई थी और अनुभवी एनिमेटर भीमसेन और राम मोहन ने फिल्म बनाने में महत्वपूर्ण भूमिका निभाई थी। इसके लिए उन्हें क्लेयर वीक्स द्वारा प्रशिक्षित किया गया और फिल्म डिवीजन की कार्टून यूनिट में अपना करियर शुरू किया।</p>

						<p style="color:#000;"> जब इस परियोजना के लिए क्लेयर सप्ताह भारत आए, तो उन्होंने डिज्नी स्टूडियो में 16 साल से अधिक समय बिताया और उन्हें स्नो व्हाइट, सिंड्रेला और बांबी जैसे क्लासिक्स बनाने का श्रेय दिया गया।</p>

						<p style="color:#000;"> एक उचित एनीमेशन स्टूडियो प्रक्रिया में बनाई गई पहली भारतीय एनीमेशन फिल्म द बरगद हिरण थी जो 1957 में बनी थी।</p>

