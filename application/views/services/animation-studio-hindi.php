						<p> अगर हम व्यक्तिगत प्रयासों की बात करें तो भारत में यह दादा साहब के अलावा किसी और ने नहीं किया था। उनके 1912 के लघु द ग्रोथ ऑफ ए पी प्लांट ने टाइम-लैप्स फोटोग्राफी की अवधारणा पेश की, जिसमें प्रति दिन एक फ्रेम शॉट था। 1915 में उन्होंने एनिमेटेड शॉर्ट अगकाड्यांची मौज (मैचस्टिक्स फन) का निर्माण किया। वह शायद एमिल कोहल की माचिस की तीली फिल्म से प्रेरित थे। दुर्भाग्य से, अगकाड्यांची मौज और विचित्र शिल्पा जैसी एनिमेटेड कृतियाँ समय के कहर से नहीं बची हैं।</p>

						<p> हालांकि एनएफडीसी (राष्ट्रीय फिल्म प्रभाग निगम) भारत में एक अग्रणी एनीमेशन स्टूडियो रहा है, लेकिन आज बहुत सारे एनीमेशन स्टूडियो आ गए हैं जो न केवल राष्ट्रीय बल्कि अंतर्राष्ट्रीय परियोजनाओं पर भी काम करते हैं।</p>

						<p>जबकि पहले भारतीय चैनलों पर दिखाई जाने वाली ज्यादातर एनिमेशन सीरीज का निर्माण मुख्य रूप से डिज्नी जैसी अंतरराष्ट्रीय कंपनियों द्वारा किया जाता था। डक टेल्स और एवर ग्रीन टॉम एंड जेरी शो जैसे शो भारत में काफी लोकप्रिय थे। लेकिन बदलते समय के साथ भारतीय स्टूडियो ने भारतीय चरित्र और भारतीय कहानियों और सामग्री पर ध्यान देना शुरू कर दिया। भारतीय एनिमेशन स्टूडियो द्वारा बनाई गई कुछ सबसे सफल एनीमेशन श्रृंखलाओं में छोटा भीम, विक्रम बेताल, चाचा बतिजा, और इसी तरह की श्रृंखलाएं शामिल हैं। इस तरह की श्रृंखला में भारतीय निर्माता हैं और उन्होंने कई पुरस्कार भी जीते हैं और इस तरह की सामग्री के लिए नए दर्शकों का निर्माण किया है। गुणवत्तापूर्ण सामग्री पर ध्यान केंद्रित करने और नवीनतम तकनीक के उपयोग में वृद्धि ने यह सुनिश्चित किया है कि एनीमेशन क्षेत्र तेजी से विकसित हुआ है। भारतीय एनीमेशन उद्योग को स्टूडियो में विभाजित किया गया है जो मुख्य रूप से एनीमेशन क्षेत्र और स्टूडियो के लिए काम करते हैं जो कॉर्पोरेट क्षेत्र पर ध्यान केंद्रित करते हैं और मुख्य रूप से उत्पादों और सेवाओं और विज्ञापनों की व्याख्या करने वाले वीडियो व्याख्याकार का उत्पादन करते हैं। मनोरंजन क्षेत्र की पूर्ति करने वाले प्रमुख स्टूडियो में हमारे पास कॉसमॉस-माया, ग्रीन गोल्ड एनिमेशन, टून्ज़ एनिमेशन और डिजिटूनज़ मीडिया जैसी कंपनियां हैं। इन भारतीय एनिमेशन स्टूडियो ने भारतीय शुरुआत में महत्वपूर्ण भूमिका निभाई है और इनमें बापू, गुड्डू, गैजेट गुरु गणेश, छोटा भीम और नवीनतम श्रृंखला दबंग (जो इसी नाम से फिल्म से एक अपटेक है) जैसी संपत्तियां हैं।</p>

