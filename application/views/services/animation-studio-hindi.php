						<p> 2डी और 3डी एनिमेशन का लुक और फील काफी अलग है। यदि आप मुख्य धारा की फिल्मों को देखें, तो 2डी एनिमेशन के कुछ उदाहरण हैं सिंड्रेला, द लायन किंग (1994), मुलान (1998) और लिटिल मरमेड जैसी फिल्में 2डी एनिमेशन के कुछ उदाहरण हैं। जबकि ये 2D फिल्में कंप्यूटर का उपयोग करके बनाई गई हैं, पुराने युग की कुछ 2D एनीमेशन फिल्में जैसे वन हंड्रेड एंड वन डालमेटियन (1961) और एलिस इन वंडरलैंड (1951) क्लासिक सेल एनीमेशन तकनीकों का उपयोग करके बनाई गई थीं, जहां प्रत्येक बाद के फ्रेम को किसके द्वारा खींचा गया था गति का भ्रम पैदा करने के लिए हाथ। </p>
						<p> दूसरी ओर मिनियंस, इनक्रेडिबल्स 2, फाइंडिंग डोरी और फ्रोजन जैसी फिल्में 3डी एनिमेशन के उदाहरण हैं।</p>
						<p> 2D और 3D एनिमेशन के बीच मुख्य अंतर यह है कि 2D एनिमेशन में तत्वों को केवल X अक्ष पर या क्षैतिज रूप से आगे या पीछे की गतिविधियों का अनुकरण करने के लिए और Y अक्ष पर लंबवत (Y अक्ष पर) ले जाया जा सकता है यदि हम उन्हें ऊपर ले जाना चाहते हैं या नीचे। </p>
						<p> 3डी एनिमेशन में वस्तुओं को Z अक्ष के साथ भी ले जाया जा सकता है और व्यक्ति इसे करीब या दूर देखता है।</p>
