						<p> १८९५ और १९२० के बीच, सिनेमाई उद्योग के उदय के दौरान, विभिन्न एनीमेशन तकनीकों का विकास किया गया, जिसमें वस्तुओं, कठपुतली, मिट्टी या कटआउट के साथ स्टॉप-मोशन, और चित्रित या चित्रित एनीमेशन शामिल हैं। हाथ से तैयार किया गया एनीमेशन, ज्यादातर सेल्स पर चित्रित एनीमेशन, 20 वीं शताब्दी के अधिकांश समय में प्रमुख तकनीक थी और इसे पारंपरिक एनीमेशन के रूप में जाना जाने लगा।
</p>


						<p>अब तक का पहला 2D एनिमेशन बनाया गया था जिसे Fantasmagorie कहा जाता था, जिसे एमिल कोहल ने बनाया था। ब्लैक एंड व्हाइट कार्टून की शुरुआत एमिल द्वारा लाइव एक्शन में एक साधारण स्टिक मैन को चित्रित करने से होती है। कार्टून लगभग 75 सेकंड लंबा है और इसे बनाने में लगभग 700 अलग-अलग चित्र हैं। यह ऐतिहासिक एनिमेशन 1908 में जारी किया गया था।
 </p>
						<p> इस बार एनिमेशन स्टूडियो की शुरुआत भी देखी गई। दुनिया के पहले एनिमेशन स्टूडियो की स्थापना राउल बैरे और उनके स्टूडियो, बैरे स्टूडियो ने वर्ष 1914 में की थी। हालांकि कुछ सूत्रों का कहना है कि इसका श्रेय ब्रे प्रोडक्शंस के प्रतिद्वंद्वी को जाना चाहिए।
 </p>

