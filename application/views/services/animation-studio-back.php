<h2>Animation Studio In India 2021</h2>
						<p style="color:#000;"> In India the pioneers of <a class="text-primary" href="https://videoexplainermumbai.in/"> animation studios</a> have been The Nation Films Division of India. They started India’s first dedicated animation studio in
							the year 1957 with the production of The Banyan Deer. This studio located in Mumbai heralded the era of animation for the country. </p>

						<p style="color:#000;"> The film was made by the Films Division of India and Veteran animators Bhimsain& Ram Mohan were instrumental in making the film. For this they were trained by Clair Weeks and started their career in Film Division’s
							Cartoon Unit.</p>

						<p style="color:#000;"> When Clair weeks came to India for this project, he had spend over 16 years in Disney Studios and was credited in the making of classics like Snow White, Cinderella and Bambi.</p>

						<p style="color:#000;"> The first Indian animation film made in a proper animation studio process was The Banyan Deer made in 1957.</p>

