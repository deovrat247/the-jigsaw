						<h3> 3D Animation </h3>

						<p> 3D animation studios mainly work using software like Blender, 3D Max or Maya. It can also be used to convey stories, make entertaining films, describe the working of machinery or process and so on. </p>
						<p> In 3D animation computer made objects are able to move in 3D space. They can move and rotate like a real life 3D object. </p>

						<h3> 2D animation VS 3D animation </h3>

						<p> The look and feel of 2D and 3D animation is quite different. If you look at main stream movies, some of the examples of 2D animation are movies like Cinderella,The Lion King (1994), Mulan (1998) and the Little Mermaid are few examples
							of 2D animation. While these 2D movies have been made using computers, some of the 2D animation movies of the older era like One Hundred and One Dalmatians (1961) and Alice in Wonderland (1951) were made using classic cell animation
							techniques where each subsequentframe was drawn by hand to create the illusion of motion. </p>
						<p> On the other hand movies like , while Minions,Incredibles 2, Finding Doryand Frozen are examples of 3D animation. </p>
						<p> The main difference between 2D and 3D animation is that in 2D animations elements can only be moved on the X axis or horizontally, to simulate forward or backward movements and on the Y axis vertically (on the Y axis) if we want them
							to
							move up or down. </p>
						<p> In 3D animations objects can also be moved along the Z axis and the person sees it closer or further away.</p>
						<p> To put it simply, 3D animation involves three dimensions X,Y and Z, while a 2D animation has only two dimensions X and Y. </p>
						<p>The objects in a 2D animation are merely flat, such as a picture or a photograph. In 3D animation elements are like objects that we see in real life and they have a front, back, top and bottom. You can see them from any angle.</p>
